int	ft_iterative_factorial(int nb)
{
	int n;

	n = 1;
	if (nb == 0)
		n = 1;
	if (nb > 0 && nb <= 12)
	{
		while (nb != 1)
		{
			n = n * nb;
			nb--;
		}
	}
	if (nb < 0 && nb > 12)
		n = 0;
	return (n);
}
